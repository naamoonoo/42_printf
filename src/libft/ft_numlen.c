#include "libft.h"

int	ft_numlen(int64_t n, int base)
{
	if (n < 0)
		n *= -1;
	if (n < (int64_t)base)
		return (1);
	else
		return (ft_numlen(n % base, base) + ft_numlen(n / base, base));
}
